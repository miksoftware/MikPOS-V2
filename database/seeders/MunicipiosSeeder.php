    /**
     * Función auxiliar para crear municipios en un departamento específico
     */
    private function crearMunicipios($departamentoId, $municipios)
    {
        foreach ($municipios as $municipio) {
            Municipio::create([
                'departamento_id' => $departamentoId,
                'nombre' => $municipio['nombre'],
                'codigo' => $municipio['codigo'],
            ]);
        }
    }
